package PatternQuestion;

public class Triangle3 {
    public static void Triange(){
        for(int i=0;i<5;i++){
            for(int j=1;j<=1+i;j++){
                System.out.print(j+" ");
            }

            System.out.println();
        }
    }
    public static void main(String[] args) {
        Triange();
    }
    
}
